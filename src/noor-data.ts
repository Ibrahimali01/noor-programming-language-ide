// دالة جلب require سيادي لمنع مشاكل البيئة السحابية والسرعات العالية
function checkNoorRequire(): any {
  if (typeof window !== 'undefined') {
    return () => { throw new Error('require is not available in browser environments.'); };
  }
  const req = (globalThis as any).noorRequire || (typeof require !== 'undefined' ? require : null);
  if (!req) {
    try {
      const moduleObj = Function('try { return require("module"); } catch(e) { return null; }')();
      if (moduleObj && moduleObj.createRequire) {
        return moduleObj.createRequire(import.meta.url);
      }
    } catch (_) {}
    return () => { throw new Error('require is not defined.'); };
  }
  return req;
}

// 🌐 نظام الفهرسة والترجمة التلقائية والشاملة لكافة الـ 250 مكتبة في مجلد stdlib
export function getFullIndexedLibraries(): LibraryMetadata[] {
  const list = [...STDLIB_INDEX];
  try {
    if (typeof window === 'undefined') {
      const fs = checkNoorRequire()('fs');
      const path = checkNoorRequire()('path');
      const stdlibDir = path.join(process.cwd(), 'stdlib');
      
      if (fs.existsSync(stdlibDir)) {
        const files = fs.readdirSync(stdlibDir).filter((f: string) => f.endsWith('.noor'));
        for (const file of files) {
          const name = file.replace('.noor', '');
          // تجنب تكرار المكتبات المفهرسة يدوياً
          if (list.some(lib => lib.name === name || lib.name === name.replace('std_', ''))) {
            continue;
          }
          
          const filePath = path.join(stdlibDir, file);
          const content = fs.readFileSync(filePath, 'utf8');
          const lines = content.split('\n');
          
          let displayName = name.replace('std_', '').replace('_', ' ');
          let mainComment = '';
          const keywords: string[] = [];
          
          let commentCount = 0;
          for (const line of lines) {
            const trimmed = line.trim();
            if (trimmed.startsWith('#')) {
              commentCount++;
              if (commentCount === 1) {
                displayName = trimmed.slice(1).trim();
              } else if (commentCount === 2) {
                mainComment = trimmed.slice(1).trim();
              }
            }
            
            const funcMatch = line.match(/دالة\s+([\p{L}\p{M}\p{N}_]+)\s*\(/u);
            if (funcMatch) {
              keywords.push(funcMatch[1]);
            }
          }
          
          let classification = 'مكتبات عامة / General';
          const lowerName = name.toLowerCase();
          if (/game|audio|color|graphic|layout|three|canvas/i.test(lowerName)) {
            classification = 'ألعاب ورسوميات / Games & Graphics';
          } else if (/ai|ml|neural|nlp|chatbot|vision|deep|tensorflow|pytorch/i.test(lowerName)) {
            classification = 'ذكاء اصطناعي / Artificial Intelligence';
          } else if (/db|sql|redis|mongo|postgres|cassandra|neo4j|elastic|prisma/i.test(lowerName)) {
            classification = 'قواعد بيانات / Databases';
          } else if (/aws|cloud|azure|gcp|server|http|express|socket|net|network/i.test(lowerName)) {
            classification = 'خوادم وشبكات / Servers & Networks';
          } else if (/crypto|security|hash|jwt|auth|firewall|guard/i.test(lowerName)) {
            classification = 'أمن وتشفير / Security & Cryptography';
          } else if (/os|fs|process|system|zip|daemon|terminal|file/i.test(lowerName)) {
            classification = 'نظام وأتمتة / OS & Automation';
          }
          
          list.push({
            name,
            classification,
            primaryFunction: mainComment || `${displayName} - مكتبة سيادية متطورة بلغة نور المستقلة.`,
            keywords: keywords.length > 0 ? keywords : [name]
          });
        }
      }
    }
  } catch (err) {
    console.warn("[STDLIB INDEX REGISTRY] Dynamic load error:", err);
  }
  return list;
}
